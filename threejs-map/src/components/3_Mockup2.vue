      </p>
      <p>
        The feedback concerning the navigation was mostly positive, while the unusual nature was a little off-putting for the participants with UI and UX experience. The need for a navigation with enhanced filtering options is present, but they would value a softer learning curve to get started with such a system.
      </p>
