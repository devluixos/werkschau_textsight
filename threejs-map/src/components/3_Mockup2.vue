      <h3>Critical Evaluation</h3>
      <p>
        The users generally interacted positively with the mock-up, appreciating the design and simple interaction possibilities. This was the aim of this iteration, to optimise the user interaction and the visualisation strategies. However, the static nature of the mock-up, inherent due to the conceptual stage of a Figma prototype, posed challenges for the participants.
      </p>
      <p>
        The mock-up, being a conceptual representation, did not have full functionality implemented. This was understood to be a limitation in this phase of development and testing. This static nature required participants to imagine the dynamic visualisation and interaction with it, which increased the cognitive load on them during the evaluations. This mental simulation of browsing through dynamic pieces of information floating around a planet impacted the fluidity of their experience and their feedback.
      </p>
